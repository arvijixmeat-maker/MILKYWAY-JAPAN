/* One-off generator: scope the MyRealTrip admin prototype CSS under .admin-app-wrapper.
   Reads the prototype admin.css, strips global resets, prefixes every component
   selector with the admin scope, and prepends the design tokens.
   Run: node scripts/gen-admin-css.cjs <path-to-admin.css> <out.css> */
const fs = require('fs');

const SRC = process.argv[2];
const OUT = process.argv[3];
const SCOPE = '.admin-app-wrapper';

let css = fs.readFileSync(SRC, 'utf8');
// Drop everything before the first component-ish rule (global resets, :root, body).
const startIdx = css.indexOf('.material-symbols-outlined {');
if (startIdx === -1) throw new Error('anchor not found');
css = css.slice(startIdx);

function matchBrace(str, openIdx) {
  let depth = 0;
  for (let k = openIdx; k < str.length; k++) {
    if (str[k] === '{') depth++;
    else if (str[k] === '}') { depth--; if (depth === 0) return k; }
  }
  return str.length - 1;
}

function prefixSelector(sel) {
  return sel.split(',').map(s => {
    s = s.trim();
    if (!s) return s;
    if (s.startsWith(SCOPE)) return s;
    return SCOPE + ' ' + s;
  }).join(',\n');
}

function processBlock(str) {
  let out = '';
  let i = 0;
  const n = str.length;
  while (i < n) {
    const ch = str[i];
    if (ch === '/' && str[i + 1] === '*') {           // comment
      const end = str.indexOf('*/', i + 2);
      const e = end === -1 ? n : end + 2;
      out += str.slice(i, e); i = e; continue;
    }
    if (/\s/.test(ch)) { out += ch; i++; continue; }  // whitespace
    if (ch === '@') {                                  // at-rule
      let j = i;
      while (j < n && str[j] !== '{' && str[j] !== ';') j++;
      const prelude = str.slice(i, j);
      if (str[j] === ';') { out += prelude + ';'; i = j + 1; continue; }
      const bodyEnd = matchBrace(str, j);
      const body = str.slice(j + 1, bodyEnd);
      const kw = prelude.trim().split(/\s/)[0].toLowerCase();
      if (kw === '@keyframes' || kw === '@-webkit-keyframes' || kw === '@font-face') {
        out += prelude + '{' + body + '}';            // leave inner verbatim
      } else {
        out += prelude + '{' + processBlock(body) + '}'; // @media/@supports → recurse
      }
      i = bodyEnd + 1; continue;
    }
    // normal rule
    let j = i;
    while (j < n && str[j] !== '{') j++;
    const sel = str.slice(i, j);
    const bodyEnd = matchBrace(str, j);
    const body = str.slice(j + 1, bodyEnd);
    out += prefixSelector(sel) + ' {' + body + '}';
    i = bodyEnd + 1; continue;
  }
  return out;
}

